# Can use single or double quotes for strings.

puts "Hello world."
puts 'Hello world.'

puts "Juliet said 'Goodbye' to Romeo."
puts 'Juliet said "Goodbye" to Romeo.'

# puts "Juliet said "Goodbye" to Romeo" # Would show an error, the Goodbye needs to be escaped.

puts "Juliet said \"Goodbye\" to Romeo." # Ruby will ignore the Goodbye error as shown above as the quotes are escaped.

puts "Let's add a line break\nright here." # Performs a line break after the word break

puts "\tInsert a tab here." # \t Puts a tab space for whatever comes after.


