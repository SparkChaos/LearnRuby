p 20.between?(10, 30) # Returns true as 20 is between 10 & 30.
p 20.between?(30, 50) # Returns false as 20 is not between 30 & 50.
p 1.2.between?(1.1, 1.3) # Returns true as 1.2 is between 1.1 & 1.3
p -10.5.between?(-20, 0) # Returns true as -10.5 is between -20 & 0
p 5.3.between?(8.8, 9.6) # Returns false as 5.3 is not between 8.8 & 9.6